public class BasicUtils 
{
	private static final String[] imgType = {".jpg",".jpeg",".bmp",".gif",".png"};
	/**
	 * 是否为图片类型
	 * @param fileType
	 * @return
	 */
	public static boolean isImg(String fileType)
	{
		boolean boo = false;
		
		for(int i=0;i<imgType.length;i++)
		{
			if(fileType.equals(imgType[i]))
			{
				boo = true;
				break;
			}
		}
		return boo;
	}
}
